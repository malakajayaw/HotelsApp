package com.example.myapplication;

public class Hotel {
    String Hname;
    String Hadress;
    String Hdistrict;
    int Hgrade;

    public Hotel() {
    }
    public String getHname() {
        return Hname;
    }

    public void setHname(String hname) {
        Hname = hname;
    }

    public String getHadress() {
        return Hadress;
    }

    public void setHadress(String hadress) {
        Hadress = hadress;
    }

    public String getHdistrict() {
        return Hdistrict;
    }

    public void setHdistrict(String hdistrict) {
        Hdistrict = hdistrict;
    }

    public int getHgrade() {
        return Hgrade;
    }

    public void setHgrade(int hgrade) {
        Hgrade = hgrade;
    }
}
